#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	uc;

	i = 0;
	uc = (char)c;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == c)
		return ((char *)s + i);
	return (0);
}
